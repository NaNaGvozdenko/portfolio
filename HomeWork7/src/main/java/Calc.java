public class Calc {
    public static int sum(int a, int b) {
        return a + b;
    }

    public static int sum(int a, int b, int c) {
        return a + b + c;
    }

    public static int diff(int a, int b) {
        return a - b;
    }

    public static long pow(int a, int b) {
        return a * b;
    }
}
